    private void btnconsuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconsuActionPerformed
        ArrayList<CuentasPorPagar> cPP;
        cPP = Archivo.leer("cPPEXC.dat");
        int po = -1;
        int id = Integer.parseInt(txtconsu.getText());
        try {
            po = Busqueda.busArrayList(cPP, id);
        } catch (IOException ex) {
            Logger.getLogger(DlgBajasEA.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (po != -1) {
            JOptionPane.showMessageDialog(this, ((EmpleadoPorComision) cPP.get(po)).toString());
        } else {
            JOptionPane.showMessageDialog(this, "El id=" + id + " no existe");
        }

    }//GEN-LAST:event_btnconsuActionPerformed

